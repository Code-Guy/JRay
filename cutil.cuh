inline __device__ double fade(double t) { return t * t * t * (t * (t * 6 - 15) + 10); }
inline __device__ double lerp(double t, double a, double b) { return a + t * (b - a); }
inline __device__ double grad(int hash, double x, double y, double z)
{
	int h = hash & 15;
	double u = h < 8 ? x : y, v = h < 4 ? y : h == 12 || h == 14 ? x : z;
	return ((h & 1) == 0 ? u : -u) + ((h & 2) == 0 ? v : -v);
}